    mapid = GameBoyEnv.pyboy.memory[0xD35E]
    xcoord = GameBoyEnv.pyboy.memory[0xD362]
    ycoord = GameBoyEnv.pyboy.memory[0xD361]
    coords = (mapid, xcoord, ycoord)

    return coords, mapid

