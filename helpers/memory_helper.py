    poke1maxhp1 = GameBoyEnv.pyboy.memory[0xD18D]
    poke1maxhp2 = GameBoyEnv.pyboy.memory[0xD18E]
    poke1currenthp1 = GameBoyEnv.pyboy.memory[0xD16C]
    poke1currenthp2 = GameBoyEnv.pyboy.memory[0xD16D]
